using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace assignments
{
    internal class AtoZ
    {
        static void Main(string[] args)
        {    // print all the alphabets from A to Z using for loop.
        
            for  (char c = 'A'; c<= 'Z'; c++)
            {
                Console.WriteLine(c);
            }
        }
    }
}
